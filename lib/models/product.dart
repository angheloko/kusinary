class Product {
  String id;
  String sellerId;
  String sellerName;
  String title;
  String description;
  String thumbnailImage;
  String ingredients;
  double price;
  int servingSize;
  double rating;

  Product({
    this.id,
    this.sellerId,
    this.sellerName,
    this.title,
    this.description,
    this.thumbnailImage,
    this.ingredients,
    this.price,
    this.servingSize,
    this.rating,
  });
}
